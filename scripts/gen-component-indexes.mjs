#!/usr/bin/env node
// gen-component-indexes.mjs — Phase 5.1 per-component index.ts re-export gen
//
// Why: re-enable `./components/*` + `./patterns/*` subpath imports in package.json exports map
//   (npm package consumers can do `import { Button } from '@qijenchen/design-system/components/Button'`)
//
// vite lib build with `preserveModules` outputs `dist/components/Button/button.js`(kebab-case),
// NOT `index.js`. exports map references `*/index.js`. Solution: add `src/components/Button/index.ts`
// that does `export * from './button'`. vite emits `dist/components/Button/index.js` matching exports.
//
// Convention: each <Name>/ has <kebab-name>.tsx as primary entry. Index re-exports that.
// Special cases(Menu/menu-item, SelectionControl/{boolean-value,selection-item}): no <kebab>.tsx
// main → fallback re-exports ALL non-stories .tsx units(2026-07-14 Dim 44 fix,原「取第一個」
// 在多 primitive dir 靜默漏 export)。

import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = join(__dirname, '..')
const SRC_ROOT = join(REPO_ROOT, 'packages/design-system/src')
const TARGETS = ['components', 'patterns']
const CHECK_MODE = process.argv.includes('--check')

function pascalToKebab(name) {
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
}

// 2026-07-14 audit Dim 44 fix:回傳「全部主單元 stems」而非單一。原 fallback 取字母序第一個
// 非 stories .tsx,在多 primitive dir 會靜默漏 export —— 錨例:SelectionControl 於 2026-07-09
// 加入 boolean-value.tsx 後,字母序把 selection-item 擠出 index,ui-development.md 宣告的
// 「internal 經 per-component subpath 消費」路徑對 SelectionItem 靜默斷鏈。
// 慣例命中(<kebab>.tsx / <Dir>.tsx)仍回單一主檔(feature-sibling 邏輯照舊);
// fallback 才展開為全部合格單元。
function findMainStems(dirPath, dirName) {
  const files = readdirSync(dirPath)
  const kebab = pascalToKebab(dirName)
  if (files.includes(kebab + '.tsx')) return [kebab]
  if (files.includes(dirName + '.tsx')) return [dirName]
  // Fallback: ALL non-stories / non-demo .tsx(sorted for deterministic output)
  return files
    .filter(
      (f) =>
        f.endsWith('.tsx') &&
        !f.includes('.stories.') &&
        !f.includes('_demo') &&
        !f.startsWith('_'),
    )
    .map((f) => f.replace(/\.tsx$/, ''))
    .sort()
}

// 2026-07-04 Q5 拍板:sibling `*-group.tsx`(ButtonGroup/ButtonDivider、CheckboxGroup)是
// spec/principles 當 consumer API 教的公開 group 子元件(MUI ButtonGroup / Ant Space.Compact /
// Chakra CheckboxGroup / Mantine Checkbox.Group 全 public),原 generator 只 export 主檔 =
// 文件教得到、import 拿不到的機械缺口。約定:`<dir 內任意>-group.tsx` 一律隨 index 一併 re-export。
// 2026-07-14 拆檔補洞:同 findFeatureSiblings,檔頭 10 行含 `@internal` marker 者排除
// (錨例:DataTable/data-table-filter-group.tsx 是 @internal renderer 非公開 group 子元件;
//  無此排除 → internal 被 index re-export 進 subpath + root barrel = 違 dim-72 internal canonical)。
function findGroupSiblings(dirPath, mainStem) {
  return readdirSync(dirPath)
    .filter((f) => {
      if (!f.endsWith('-group.tsx') || f.includes('.stories.') || f === mainStem + '.tsx') return false
      const head = readFileSync(join(dirPath, f), 'utf8').split('\n').slice(0, 10).join('\n')
      return !head.includes('@internal')
    })
    .map((f) => f.replace(/\.tsx$/, ''))
    .sort()
}

// 2026-07-08 WM root-cause 戰役(DT-EXPORT 斷鏈):spec 教學的公開 sub-feature 檔
// (data-table-filter-panel / sort-manager / column-visibility-panel 等 `<mainStem>-*.tsx`)
// 原 index 不 re-export → npm consumer「文件教得到、import 拿不到」,WM 被迫整包重造
// FilterPanel 350 行(-group.tsx 同款缺口第二次實證)。約定:`<mainStem>-*.tsx` 一律隨 index
// re-export;排除 .stories. / `-story-helpers.tsx` / 檔頭 10 行含 `@internal` marker。
function findFeatureSiblings(dirPath, mainStem) {
  return readdirSync(dirPath)
    .filter((f) => {
      if (!f.startsWith(mainStem + '-') || !f.endsWith('.tsx')) return false
      if (f.includes('.stories.') || f.endsWith('-group.tsx') || f.endsWith('-story-helpers.tsx')) return false
      if (f === mainStem + '.tsx') return false
      const head = readFileSync(join(dirPath, f), 'utf8').split('\n').slice(0, 10).join('\n')
      return !head.includes('@internal')
    })
    .map((f) => f.replace(/\.tsx$/, ''))
    .sort()
}

function generateIndexContent(mainStems, groupStems = []) {
  const stems = [...new Set([...mainStems, ...groupStems])]
  return [
    '// AUTO-GENERATED by scripts/gen-component-indexes.mjs — DO NOT EDIT MANUALLY.',
    '// Re-export pattern enables package.json exports map subpath:',
    "//   import { ... } from '@qijenchen/design-system/{components,patterns}/<Dir>'",
    '// Regenerate: node scripts/gen-component-indexes.mjs',
    ...stems.map((g) => "export * from './" + g + "'"),
    '',
  ].join('\n')
}

function collectTargets() {
  const out = []
  for (const target of TARGETS) {
    const targetRoot = join(SRC_ROOT, target)
    if (!existsSync(targetRoot)) continue
    for (const entry of readdirSync(targetRoot, { withFileTypes: true })) {
      if (!entry.isDirectory()) continue
      if (entry.name === 'Internal') {
        // Internal/ group dir — recurse 1 level
        const internalRoot = join(targetRoot, entry.name)
        for (const inner of readdirSync(internalRoot, { withFileTypes: true })) {
          if (!inner.isDirectory()) continue
          const innerPath = join(internalRoot, inner.name)
          const mains = findMainStems(innerPath, inner.name)
          if (mains.length) out.push({ mainStems: mains, indexPath: join(innerPath, 'index.ts'), dirPath: innerPath })
        }
        continue
      }
      const dirPath = join(targetRoot, entry.name)
      const mains = findMainStems(dirPath, entry.name)
      if (mains.length) out.push({ mainStems: mains, indexPath: join(dirPath, 'index.ts'), dirPath })
    }
  }
  return out
}

function main() {
  const targets = collectTargets()
  let driftCount = 0
  let writeCount = 0

  for (const { mainStems, indexPath, dirPath } of targets) {
    // EXTRA_SUBPATH_STEMS:internal 型別通道(2026-06-05 拍板「internal 需 subpath 包裝後
    // 消費」)的機械落實 — 這些 .ts 模組不進通用 sibling 規則(僅 .tsx),explicit 列名。
    // root barrel 側由 /Internal$/ 名稱過濾擋前門(gen-design-system-barrel.mjs),subpath-only。
    const EXTRA_SUBPATH_STEMS = { Field: ['field-types'] }
    const dirName = dirPath.split('/').pop()
    const expected = generateIndexContent(mainStems, [
      ...mainStems.flatMap((stem) => findGroupSiblings(dirPath, stem)),
      ...mainStems.flatMap((stem) => findFeatureSiblings(dirPath, stem)),
      ...(EXTRA_SUBPATH_STEMS[dirName] ?? []),
    ])
    const existing = existsSync(indexPath) ? readFileSync(indexPath, 'utf8') : null
    if (CHECK_MODE) {
      if (existing !== expected) {
        console.error('DRIFT: ' + indexPath.replace(REPO_ROOT + '/', ''))
        driftCount++
      }
    } else {
      if (existing !== expected) {
        writeFileSync(indexPath, expected)
        writeCount++
      }
    }
  }

  if (CHECK_MODE) {
    if (driftCount > 0) {
      console.error('')
      console.error(driftCount + ' index.ts files out of sync.')
      console.error('Run: node scripts/gen-component-indexes.mjs')
      process.exit(1)
    }
    console.log(targets.length + ' component/pattern index.ts files in sync.')
    return
  }
  console.log('Generated/updated ' + writeCount + ' index.ts (of ' + targets.length + ' target dirs).')
  console.log('Skipped ' + (targets.length - writeCount) + ' already in sync.')
}

main()
